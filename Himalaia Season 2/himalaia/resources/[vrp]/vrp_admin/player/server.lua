function SendWebhookMessage(webhook,message)
	if webhook ~= nil and webhook ~= "" then
		PerformHttpRequest(webhook, function(err, text, headers) end, 'POST', json.encode({content = message}), { ['Content-Type'] = 'application/json' })
	end
end

-----------------------------------------------------------------------------------------------------------------------------------------
-- CHECK VIP
-----------------------------------------------------------------------------------------------------------------------------------------
function src.checkVIP()
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		if vRP.hasPermission(user_id,"vip.permissao") or vRP.hasPermission(user_id,"ceo.permissao") then
			return true
		else
			TriggerClientEvent("Notify",source,"negado","Você não possui <b>Vip</b> para fazer essa ação.") 
			return false
		end
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- CHECK ATTACHS
-----------------------------------------------------------------------------------------------------------------------------------------
function src.checkAttachs()
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		if vRP.getInventoryItemAmount(user_id,"compattach") >= 1 or vRP.hasPermission(user_id,"booster.permissao") or vRP.hasPermission(user_id,"vip.permissao") or vRP.hasPermission(user_id,"ceo.permissao") then
			return true
		else
			TriggerClientEvent("Notify",source,"negado","Você não possui <b>Modificador de Armas</b> para fazer essa ação.") 
			return false
		end
	end
end

